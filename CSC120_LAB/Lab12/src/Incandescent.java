public class Incandescent extends Light {
    //-------------------------------------------------------------------------------------------------
    public Incandescent(int lumens) {
        super(lumens);
    }
    //-------------------------------------------------------------------------------------------------
    @Override
    public double heatOutput() {
//----Heat output is 87.4 times the lumens
        return getLumens() * 87.4;
    }
    //-------------------------------------------------------------------------------------------------
    @Override
    public boolean isFlourescent() {
        return false;
    }
//-----------------------------------------------------------------------------------------------------
}
