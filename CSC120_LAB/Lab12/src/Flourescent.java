//=================================================================================================
public class Flourescent extends Light {
   //----------------------------------------------------------------------------------------------
    public Flourescent(int lumens) {
        super(lumens);
    }
    //---------------------------------------------------------------------------------------------
    @Override
    public double heatOutput() {
//----Flourescent lights produce no heat
        return 0.0;
    }
    //---------------------------------------------------------------------------------------------
    @Override
    public boolean isFlourescent() {
        return true;
    }
//-------------------------------------------------------------------------------------------------
}
